/*
  One-command RAG optimization pipeline:
    1) Generate dataset from current business knowledge
    2) Validate dataset schema
    3) Run retrieval tuner
    4) Export recommended env overrides to .env.rag.best

  Usage:
    RAG_EVAL_BUSINESS_ID=<business_id> npm run rag:optimize
*/

const fs = require("fs");
const path = require("path");
const { spawnSync } = require("child_process");

const BUSINESS_ID = process.env.RAG_EVAL_BUSINESS_ID || "";
const DATASET_PATH = process.env.RAG_EVAL_DATASET || "scripts/rag-retrieval-eval.generated.json";
const TUNE_REPORT = process.env.RAG_TUNE_OUTPUT_FILE || "scripts/rag-tuning.result.json";
const ENV_OUTPUT = process.env.RAG_ENV_OUTPUT_FILE || ".env.rag.best";

function runNodeScript(scriptName, extraEnv = {}) {
  const scriptPath = path.join(process.cwd(), "scripts", scriptName);
  const result = spawnSync(process.execPath, [scriptPath], {
    env: { ...process.env, ...extraEnv },
    stdio: "inherit",
    shell: false,
  });

  if (result.status !== 0) {
    throw new Error(`${scriptName} failed with exit code ${result.status}`);
  }
}

function writeBestEnv(bestConfig) {
  const lines = [
    "# Generated by scripts/optimize-rag-pipeline.js",
    "# Apply selectively to your runtime env",
    `RAG_RETRIEVAL_TOP_K=${bestConfig.topK}`,
    `RAG_MIN_VECTOR_SIMILARITY=${bestConfig.minVector}`,
    `RAG_MULTI_QUERY_ENABLED=${bestConfig.multiQuery}`,
    `RAG_MULTI_QUERY_MAX_VARIANTS=${bestConfig.maxVariants}`,
    "",
  ];

  const outputPath = path.isAbsolute(ENV_OUTPUT) ? ENV_OUTPUT : path.join(process.cwd(), ENV_OUTPUT);
  fs.writeFileSync(outputPath, lines.join("\n"), "utf8");
  return outputPath;
}

function run() {
  if (!BUSINESS_ID) {
    console.error("[RagOptimize] Missing RAG_EVAL_BUSINESS_ID");
    process.exit(1);
    return;
  }

  console.log("[RagOptimize] Step 1/4 - Generating dataset");
  runNodeScript("generate-rag-retrieval-dataset.js", {
    RAG_EVAL_BUSINESS_ID: BUSINESS_ID,
    RAG_EVAL_OUTPUT: DATASET_PATH,
  });

  console.log("[RagOptimize] Step 2/4 - Validating dataset");
  runNodeScript("validate-rag-retrieval-dataset.js", {
    RAG_EVAL_DATASET: DATASET_PATH,
  });

  console.log("[RagOptimize] Step 3/4 - Running tuner");
  runNodeScript("tune-rag-retrieval.js", {
    RAG_EVAL_BUSINESS_ID: BUSINESS_ID,
    RAG_EVAL_DATASET: DATASET_PATH,
    RAG_TUNE_OUTPUT_FILE: TUNE_REPORT,
  });

  const reportPath = path.isAbsolute(TUNE_REPORT) ? TUNE_REPORT : path.join(process.cwd(), TUNE_REPORT);
  if (!fs.existsSync(reportPath)) {
    throw new Error(`Tune report not found: ${reportPath}`);
  }

  const report = JSON.parse(fs.readFileSync(reportPath, "utf8"));
  const best = report && report.best && report.best.config;
  if (!best) {
    throw new Error("Tune report missing best.config");
  }

  console.log("[RagOptimize] Step 4/4 - Exporting recommended env overrides");
  const envPath = writeBestEnv(best);

  console.log("[RagOptimize] Done");
  console.log(`[RagOptimize] Dataset: ${path.resolve(DATASET_PATH)}`);
  console.log(`[RagOptimize] Tuning report: ${reportPath}`);
  console.log(`[RagOptimize] Recommended env file: ${envPath}`);
}

try {
  run();
} catch (error) {
  console.error("[RagOptimize] Error:", error && error.message ? error.message : error);
  process.exit(1);
}

